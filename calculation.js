// Menghitung Luas Persegi

exports.mult = function (side, side) {
    return side * side;
};

// Menghitung Keliling Persegi 

exports.mult = function (sides, side) {
    return sides * side;
};

// Menghitung Luas Persegi Panjang

exports.mult = function (length, width) {
    return length * width;
};

// Menghitung Keliling Persegi Panjang

exports.mult = function (length, width) {
    return 2 * (length + width);
};
